// store name of places in array not in alphabatical order

let places: string[]= ["Paris","California","Italy","Switzerland","Dubai"]

// array in its original order
console.log(places);

// array in alphabatical order
console.log([...places].sort());

// array in its original order
console.log(places);

// array in reverse order
console.log([...places].reverse());

// array back in its original order
console.log(places);

// array in alphabatical order
console.log([...places].sort());

// array again in reverse alphabetical order
console.log([...places].reverse());

